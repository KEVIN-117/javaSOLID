package Solid.N_2OpenClose;

public class AreasFiguras {
    private Float base;
    private Float altura;

    public AreasFiguras(Float base, Float altura){
        this.base = base;
        this.altura = altura;
    }

    public Float getBase() {
        return base;
    }

    public void setBase(Float base) {
        this.base = base;
    }

    public Float getAltura() {
        return altura;
    }

    public void setAltura(Float altura) {
        this.altura = altura;
    }


}
