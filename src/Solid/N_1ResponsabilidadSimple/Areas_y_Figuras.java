package Solid.N_1ResponsabilidadSimple;

public class Areas_y_Figuras {
    private Float base;
    private Float altura;

    public Areas_y_Figuras(Float base, Float altura){
        this.base = base;
        this.altura = altura;
    }

    public Float getBase() {
        return base;
    }

    public void setBase(Float base) {
        this.base = base;
    }

    public Float getAltura() {
        return altura;
    }

    public void setAltura(Float altura) {
        this.altura = altura;
    }

}
